    def _generate_chart_html(self, chart_paths: Dict[str, str]) -> str:
        """Generate HTML for charts"""
        
        chart_html = ""
        for chart_name, chart_path in chart_paths.items():
            if chart_path and Path(chart_path).exists():
                # Convert to base64 for embedding
                with open(chart_path, 'rb') as f:
                    chart_data = base64.b64encode(f.read()).decode()
                
                chart_html += f"""
                <div class="chart">
                    <h3>{chart_name.replace('_', ' ').title()}</h3>
                    <img src="data:image/png;base64,{chart_data}" alt="{chart_name}">
                </div>
                """
        
        return chart_html
    
    async def _generate_pdf_report(self, findings: List[VulnerabilityFinding],
                                 metadata: ReportMetadata,
                                 executive_summary: ExecutiveSummary,
                                 chart_paths: Dict[str, str]) -> Path:
        """Generate PDF report"""
        
        # Generate HTML first
        html_path = await self._generate_html_report(findings, metadata, executive_summary, chart_paths)
        
        # Convert HTML to PDF
        pdf_path = self.reports_dir / f"{metadata.report_id}_report.pdf"
        
        try:
            # Configure PDF options
            options = {
                'page-size': 'A4',
                'margin-top': '0.75in',
                'margin-right': '0.75in',
                'margin-bottom': '0.75in',
                'margin-left': '0.75in',
                'encoding': "UTF-8",
                'no-outline': None,
                'enable-local-file-access': None
            }
            
            pdfkit.from_file(str(html_path), str(pdf_path), options=options)
            
        except Exception as e:
            self.logger.warning(f"Failed to generate PDF using pdfkit: {e}")
            # Fallback to ReportLab
            await self._generate_pdf_with_reportlab(findings, metadata, executive_summary, pdf_path)
        
        return pdf_path
    
    async def _generate_pdf_with_reportlab(self, findings: List[VulnerabilityFinding],
                                         metadata: ReportMetadata,
                                         executive_summary: ExecutiveSummary,
                                         pdf_path: Path):
        """Generate PDF using ReportLab as fallback"""
        
        doc = SimpleDocTemplate(str(pdf_path), pagesize=A4)
        styles = getSampleStyleSheet()
        story = []
        
        # Title
        title_style = ParagraphStyle(
            'CustomTitle',
            parent=styles['Heading1'],
            fontSize=24,
            spaceAfter=30,
            alignment=TA_CENTER
        )
        story.append(Paragraph(f"{metadata.target_name} Security Assessment Report", title_style))
        story.append(Spacer(1, 20))
        
        # Metadata
        story.append(Paragraph(f"<b>Report ID:</b> {metadata.report_id}", styles['Normal']))
        story.append(Paragraph(f"<b>Assessment Period:</b> {metadata.assessment_period}", styles['Normal']))
        story.append(Paragraph(f"<b>Generated:</b> {metadata.generated_date}", styles['Normal']))
        story.append(Spacer(1, 20))
        
        # Executive Summary
        story.append(Paragraph("Executive Summary", styles['Heading2']))
        story.append(Paragraph(executive_summary.business_impact_summary, styles['Normal']))
        story.append(Spacer(1, 20))
        
        # Vulnerability Summary Table
        story.append(Paragraph("Vulnerability Summary", styles['Heading2']))
        
        table_data = [
            ['Severity', 'Count', 'Percentage'],
            ['Critical', str(executive_summary.critical_count), f"{(executive_summary.critical_count / executive_summary.total_vulnerabilities * 100) if executive_summary.total_vulnerabilities > 0 else 0:.1f}%"],
            ['High', str(executive_summary.high_count), f"{(executive_summary.high_count / executive_summary.total_vulnerabilities * 100) if executive_summary.total_vulnerabilities > 0 else 0:.1f}%"],
            ['Medium', str(executive_summary.medium_count), f"{(executive_summary.medium_count / executive_summary.total_vulnerabilities * 100) if executive_summary.total_vulnerabilities > 0 else 0:.1f}%"],
            ['Low', str(executive_summary.low_count), f"{(executive_summary.low_count / executive_summary.total_vulnerabilities * 100) if executive_summary.total_vulnerabilities > 0 else 0:.1f}%"]
        ]
        
        table = Table(table_data)
        table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.grey),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 14),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
            ('GRID', (0, 0), (-1, -1), 1, colors.black)
        ]))
        
        story.append(table)
        story.append(PageBreak())
        
        # Detailed Findings
        story.append(Paragraph("Detailed Findings", styles['Heading2']))
        
        for i, finding in enumerate(findings, 1):
            story.append(Paragraph(f"{i}. {finding.title}", styles['Heading3']))
            story.append(Paragraph(f"<b>Severity:</b> {finding.severity.upper()}", styles['Normal']))
            story.append(Paragraph(f"<b>CVSS Score:</b> {finding.cvss_score}/10.0", styles['Normal']))
            story.append(Paragraph(f"<b>Description:</b> {finding.description}", styles['Normal']))
            story.append(Paragraph(f"<b>Impact:</b> {finding.impact}", styles['Normal']))
            story.append(Paragraph(f"<b>Remediation:</b> {finding.remediation}", styles['Normal']))
            story.append(Spacer(1, 20))
        
        # Build PDF
        doc.build(story)
    
    async def _generate_json_report(self, findings: List[VulnerabilityFinding],
                                  metadata: ReportMetadata,
                                  executive_summary: ExecutiveSummary) -> Path:
        """Generate JSON report"""
        
        report_data = {
            'metadata': asdict(metadata),
            'executive_summary': asdict(executive_summary),
            'findings': [asdict(finding) for finding in findings],
            'generated_at': datetime.now().isoformat(),
            'report_version': '1.0'
        }
        
        json_path = self.reports_dir / f"{metadata.report_id}_report.json"
        with open(json_path, 'w', encoding='utf-8') as f:
            json.dump(report_data, f, indent=2, ensure_ascii=False)
        
        return json_path
    
    async def _generate_csv_report(self, findings: List[VulnerabilityFinding],
                                 metadata: ReportMetadata) -> Path:
        """Generate CSV report"""
        
        import csv
        
        csv_path = self.reports_dir / f"{metadata.report_id}_findings.csv"
        
        with open(csv_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.writer(f)
            
            # Header
            writer.writerow([
                'ID', 'Title', 'Severity', 'CVSS Score', 'CVSS Vector',
                'Affected URL', 'Affected Parameter', 'Description',
                'Impact', 'Remediation', 'Discovery Date', 'Verification Status'
            ])
            
            # Data
            for finding in findings:
                writer.writerow([
                    finding.id,
                    finding.title,
                    finding.severity,
                    finding.cvss_score,
                    finding.cvss_vector,
                    finding.affected_url,
                    finding.affected_parameter,
                    finding.description,
                    finding.impact,
                    finding.remediation,
                    finding.discovery_date,
                    finding.verification_status
                ])
        
        return csv_path
    
