    def _generate_executive_summary(self, findings: List[VulnerabilityFinding]) -> ExecutiveSummary:
        """Generate executive summary from findings"""
        
        # Count vulnerabilities by severity
        severity_counts = {'critical': 0, 'high': 0, 'medium': 0, 'low': 0, 'info': 0}
        
        for finding in findings:
            severity = finding.severity.lower()
            if severity in severity_counts:
                severity_counts[severity] += 1
        
        # Determine overall risk rating
        if severity_counts['critical'] > 0:
            overall_risk = "Critical"
        elif severity_counts['high'] > 0:
            overall_risk = "High"
        elif severity_counts['medium'] > 0:
            overall_risk = "Medium"
        elif severity_counts['low'] > 0:
            overall_risk = "Low"
        else:
            overall_risk = "Informational"
        
        # Generate key findings
        key_findings = []
        critical_findings = [f for f in findings if f.severity.lower() == 'critical']
        high_findings = [f for f in findings if f.severity.lower() == 'high']
        
        for finding in critical_findings[:3]:  # Top 3 critical
            key_findings.append(f"Critical: {finding.title}")
        
        for finding in high_findings[:2]:  # Top 2 high
            key_findings.append(f"High: {finding.title}")
        
        # Generate recommendations
        recommendations = [
            "Immediately address all Critical and High severity vulnerabilities",
            "Implement a comprehensive security testing program",
            "Conduct regular security assessments and penetration testing",
            "Establish a vulnerability management process",
            "Provide security training for development teams"
        ]
        
        # Business impact summary
        business_impact = self._generate_business_impact_summary(findings, severity_counts)
        
        # Remediation timeline
        remediation_timeline = self._generate_remediation_timeline(severity_counts)
        
        return ExecutiveSummary(
            total_vulnerabilities=len(findings),
            critical_count=severity_counts['critical'],
            high_count=severity_counts['high'],
            medium_count=severity_counts['medium'],
            low_count=severity_counts['low'],
            info_count=severity_counts['info'],
            overall_risk_rating=overall_risk,
            key_findings=key_findings,
            recommendations=recommendations,
            business_impact_summary=business_impact,
            remediation_timeline=remediation_timeline
        )
    
    def _generate_business_impact_summary(self, findings: List[VulnerabilityFinding], 
                                        severity_counts: Dict[str, int]) -> str:
        """Generate business impact summary"""
        
        impact_parts = []
        
        if severity_counts['critical'] > 0:
            impact_parts.append(
                f"The {severity_counts['critical']} Critical vulnerabilities pose immediate "
                "risk to business operations and could result in complete system compromise, "
                "data breaches, and significant financial losses."
            )
        
        if severity_counts['high'] > 0:
            impact_parts.append(
                f"The {severity_counts['high']} High severity vulnerabilities could lead to "
                "unauthorized access to sensitive data, system manipulation, and potential "
                "regulatory compliance violations."
            )
        
        if severity_counts['medium'] > 0:
            impact_parts.append(
                f"The {severity_counts['medium']} Medium severity vulnerabilities may allow "
                "limited unauthorized access and could be chained with other vulnerabilities "
                "to increase impact."
            )
        
        # Calculate potential financial impact
        financial_impact = self._calculate_financial_impact(severity_counts)
        impact_parts.append(f"Estimated potential financial impact: {financial_impact}")
        
        return " ".join(impact_parts)
    
    def _calculate_financial_impact(self, severity_counts: Dict[str, int]) -> str:
        """Calculate estimated financial impact"""
        
        # Conservative estimates based on industry data
        impact_estimates = {
            'critical': 500000,  # $500K per critical vulnerability
            'high': 100000,     # $100K per high vulnerability
            'medium': 25000,    # $25K per medium vulnerability
            'low': 5000         # $5K per low vulnerability
        }
        
        total_impact = 0
        for severity, count in severity_counts.items():
            if severity in impact_estimates:
                total_impact += count * impact_estimates[severity]
        
        if total_impact >= 1000000:
            return f"${total_impact/1000000:.1f}M+"
        elif total_impact >= 1000:
            return f"${total_impact/1000:.0f}K+"
        else:
            return f"${total_impact:,.0f}+"
    
    def _generate_remediation_timeline(self, severity_counts: Dict[str, int]) -> str:
        """Generate remediation timeline recommendations"""
        
        timeline_parts = []
        
        if severity_counts['critical'] > 0:
            timeline_parts.append("Critical vulnerabilities: Fix within 24 hours")
        
        if severity_counts['high'] > 0:
            timeline_parts.append("High severity vulnerabilities: Fix within 72 hours")
        
        if severity_counts['medium'] > 0:
            timeline_parts.append("Medium severity vulnerabilities: Fix within 1 week")
        
        if severity_counts['low'] > 0:
            timeline_parts.append("Low severity vulnerabilities: Fix within 1 month")
        
        return "; ".join(timeline_parts)
    
